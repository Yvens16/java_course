public class pseudo_12 {
  // static veut dire que la method appartient à la classe App mais vous le verrez plus tard
  // void veut dire que la fonction ne retourne pas de value, ici elle affiche juste le résultat
  static void Pair(int number) {
    if (number % 2 == 0) {
      System.out.println(true);
    } else {
      System.out.println(false);
    }
  }
}
